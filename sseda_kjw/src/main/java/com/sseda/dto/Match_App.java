package com.sseda.dto;

public class Match_App {
	private String seqno;
	private String item_seqno;
	private String id;
	private String wdate;
	private String title;
	public String getSeqno() {
		return seqno;
	}
	public void setSeqno(String seqno) {
		this.seqno = seqno;
	}
	public String getItem_seqno() {
		return item_seqno;
	}
	public void setItem_seqno(String item_seqno) {
		this.item_seqno = item_seqno;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWdate() {
		return wdate;
	}
	public void setWdate(String wdate) {
		this.wdate = wdate;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
	
}
