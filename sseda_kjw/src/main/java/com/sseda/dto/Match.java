package com.sseda.dto;

public class Match {
	private String no;
	private String seqno;
	private String mentoring;
	private String mat_wdate;
	private String men_date;
	private String o_id;
	private String i_id;
	private String title;
	private String i_seqno;
	
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getSeqno() {
		return seqno;
	}
	public void setSeqno(String seqno) {
		this.seqno = seqno;
	}
	public String getMentoring() {
		return mentoring;
	}
	public void setMentoring(String mentoring) {
		this.mentoring = mentoring;
	}
	public String getMat_wdate() {
		return mat_wdate;
	}
	public void setMat_wdate(String mat_wdate) {
		this.mat_wdate = mat_wdate;
	}
	public String getMen_date() {
		return men_date;
	}
	public void setMen_date(String men_date) {
		this.men_date = men_date;
	}
	public String getO_id() {
		return o_id;
	}
	public void setO_id(String o_id) {
		this.o_id = o_id;
	}
	public String getI_id() {
		return i_id;
	}
	public void setI_id(String i_id) {
		this.i_id = i_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getI_seqno() {
		return i_seqno;
	}
	public void setI_seqno(String i_seqno) {
		this.i_seqno = i_seqno;
	}
	
}
